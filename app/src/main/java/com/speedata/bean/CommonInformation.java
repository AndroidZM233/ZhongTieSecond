package com.speedata.bean;

import com.elsw.base.db.orm.annotation.Column;
import com.elsw.base.db.orm.annotation.Table;

@Table(name = "commonInformation")
public class CommonInformation {

	@Column(name = "ClassNodebh")
	// 类别编码
	private String ClassNodebh;

	@Column(name = "ClassCode")
	// 上级编码
	private String ClassCode;

	@Column(name = "ClassName")
	// 类别名称
	private String ClassName;

	@Column(name = "ClassOrder")
	// 序号
	private int ClassOrder;

	@Column(name = "Remark")
	// 备注
	private String Remark;

	@Column(name = "DeleteFlag")
	// 标识码
	private Byte DeleteFlag;

	public String getClassNodebh() {
		return ClassNodebh;
	}

	public void setClassNodebh(String classNodebh) {
		ClassNodebh = classNodebh;
	}

	public String getClassCode() {
		return ClassCode;
	}

	public void setClassCode(String classCode) {
		ClassCode = classCode;
	}

	public String getClassName() {
		return ClassName;
	}

	public void setClassName(String className) {
		ClassName = className;
	}

	public int getClassOrder() {
		return ClassOrder;
	}

	public void setClassOrder(int classOrder) {
		ClassOrder = classOrder;
	}

	public String getRemark() {
		return Remark;
	}

	public void setRemark(String remark) {
		Remark = remark;
	}

	public Byte getDeleteFlag() {
		return DeleteFlag;
	}

	public void setDeleteFlag(Byte deleteFlag) {
		DeleteFlag = deleteFlag;
	}

}
